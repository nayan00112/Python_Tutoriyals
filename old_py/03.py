#min() and max()
# The min() function is used to get the lowest value of a list.
# The max() function is used to get the higest value of a list.
x = [45, 56, 32, 43, 50, 97]
print(min(x)) # 32
p = 5
q = 7
print(min(p, q)) # 5
print(min(2, 6)) # 2
print(max(x)) # 97
print(max(p, q)) # 7
print(max(2, 6, 7)) #7
